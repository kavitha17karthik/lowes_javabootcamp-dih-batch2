package Array;

public class ArrayOnemore {
    public static void main(String[] args) {
        int a[]={1,2,3,4,5};//declare..instantiate..initialize
        for(int i=0;i<a.length;i++)
            System.out.println(a[i]);

        for(int number: a){
            System.out.println(number);
        }
    }

}
